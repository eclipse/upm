#include <unistd.h>
#include <iostream>
#include <signal.h>

#include "bmpx8x.hpp"

using namespace std;

bool shouldRun = true;

void sig_handler(int signo)
{
    if (signo == SIGINT)
        shouldRun = false;
}

int main(int argc, char **argv)
{
    signal(SIGINT, sig_handler);
//! [Interesting]

    // Instantiate a BMPX8X sensor on I2C using defaults.
    upm::BMPX8X sensor;

    // Print the pressure, altitude, sea level, and
    // temperature values every 0.5 seconds
    while (shouldRun)
    {
        sensor.update();

        cout << "Pressure: "
             << sensor.getPressure()
             << " Pa, Temperature: "
             << sensor.getTemperature()
             << " C, Altitude: "
             << sensor.getAltitude()
             << " m, Sea level: "
             << sensor.getSealevelPressure()
             << " Pa"
             << endl;

        usleep(500000);
    }

    cout << "Exiting..." << endl;

//! [Interesting]
    return 0;
}
